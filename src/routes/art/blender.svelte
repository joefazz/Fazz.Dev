  <p class="my-2">
    During my time at Gravity Sketch I started hearing a lot of 3D modelling terminology and concepts. It's 
    always been an area I'm interested in, my first non paper round work experience was actually spending a week
    at Thales when I was 16 and I basically just got to play around with 3DS Max and make something.
  </p>

  <p class="my-2">
    I decided to pick up Blender because I had heard of it and, more significantly I heard that there was a lot of
    really good educational content on it on YouTube (there is).
  </p>

  <p class="my-2">
    So far I haven't done anything I would consider significantly complex or unique, at this point what I've mostly
