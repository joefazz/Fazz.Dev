  <p class="my-2">
    Sneaky Car is a game that a coworker and I were making in our spare time during 2020. The aim was to try 
    and create a top down racer inspired by the game <a href="https://store.steampowered.com/app/281280/Mashed/">Mashed</a> which I played a lot when I was young.
    During this project I had to learn a lot about the physics of cars so I could tune the handling to be fun but
    so it still took skill. 
  </p>

  <b>Fun stuff I got to try:</b>
  <ul>
    <li class="my-1 italic">- Cinemachine to make an intro cinematic for tracks Mario Kart style</li>
    <li class="my-1 italic">- Input System 2.0 for cross platform controller support</li>
    <li class="my-1 italic">- Track design and car physics tuning</li>
    <li class="my-1 italic">- Two player local coop (sort of got this for free with the Input System 2.0)</li>
  </ul>

