  <p>
    Next.js application built for my final year project of university. Gives
    each user their own private coding environment to play with/break.
  </p>

  <p>
    Project was created with Next.js, React.js, Styled Components, Docker and
    Node.js.
  </p>

  <b>Features</b>
  <ul>
    <li>Private coding environment using containers</li>
    <li>Monaco code editing experience</li>
    <li>Support for JavaScript, Python and C++ compilation</li>
    <li>Ability to create and perform exercises ala Codecademy</li>
  </ul>

